    private void btn_calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_calcularActionPerformed
        // TODO add your handling code here:
        double a = Double.parseDouble(txt_a.getText()); //aceleracion
        double v = Double.parseDouble(txt_v.getText()); //velocidad
        double t = Double.parseDouble(txt_t.getText()); //tiempo
        double resultado = v + a * t; //formula de la velocidad final
        txt_resultado.setText(String.valueOf(resultado));

    }//GEN-LAST:event_btn_calcularActionPerformed

