    // Mostrar el tiempo en el jframe arriba a izquierda en el titulo
    private void iniciarCronometro() {
        hiloTiempo = new Thread() {
            public void run() {
                try {
                    while (true) {
                        sleep(1000);
                        segundo++;
                        if (segundo == 60) {
                            segundo = 0;
                            minuto++;
                        }
                        if (minuto == 60) {
                            minuto = 0;
                            hora++;
                        }
                        setTitle("TicoBingo - " + hora + ":" + minuto + ":" + segundo);
                    }
                } catch (InterruptedException e) {
                    System.out.println("Error en el hilo");
                }
            }
        };
        hiloTiempo.start();
    }

    // detener el cronometro
    private void detenerCronometro() {
        if (hiloTiempo != null) {
            hiloTiempo.stop();
            hiloTiempo = null;
        }
    }

