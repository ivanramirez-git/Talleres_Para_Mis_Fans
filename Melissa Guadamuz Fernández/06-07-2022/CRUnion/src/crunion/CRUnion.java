    public CRUnion() {
        // Pedir Numero de monedas en un mensaje de dialogo
        String num_monedas = JOptionPane.showInputDialog("Ingrese el numero de monedas: ");
        // validar que sea un numero entero, mayor a 0 y menor a 25
        try {
            int num_monedas_int = Integer.parseInt(num_monedas);
            if (!(num_monedas_int > 0 && num_monedas_int <= 25)) {
                JOptionPane.showMessageDialog(null, "El numero de monedas debe ser mayor a 0 y menor a 25, creando una lista de equivalencias por defecto");
                conversion = new Conversion();
            } else {
                conversion = new Conversion(num_monedas_int);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El valor ingresado no es un numero, creando una lista de equivalencias por defecto");
            conversion = new Conversion();
        }
        initComponents();
        // cmbVenta, cmbCompra, cmbCambio1, cmbCambio2, vaciar
        cmbVenta.removeAllItems();
        cmbCompra.removeAllItems();
        cmbCambio1.removeAllItems();
        cmbCambio2.removeAllItems();
        // agregar las monedas a cmbVenta, cmbCompra, cmbCambio1, cmbCambio2
        for (int i = 0; i < conversion.getEquivalencias().getNum_monedas(); i++) {
            cmbVenta.addItem(conversion.getEquivalencias().getMonedas()[i]);
            cmbCompra.addItem(conversion.getEquivalencias().getMonedas()[i]);
            cmbCambio1.addItem(conversion.getEquivalencias().getMonedas()[i]);
            cmbCambio2.addItem(conversion.getEquivalencias().getMonedas()[i]);
        }
    }

