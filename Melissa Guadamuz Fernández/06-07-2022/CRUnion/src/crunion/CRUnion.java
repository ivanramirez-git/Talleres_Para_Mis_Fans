    private void btnVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVentaActionPerformed
        // Validar que no haya campos vacios en txtVenta y que el valor numerico mayor a 0
        float valor = 0;
        if (txtVenta.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un valor en el campo de venta");
            return;
        }
        try {
            valor = Float.parseFloat(txtVenta.getText());
            if (valor <= 0) {
                JOptionPane.showMessageDialog(this, "El valor debe ser mayor a 0");
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al ingresar el valor, debe ser un numero");
            return;
        }
        // Resultado
        lblVenta.setText("Por " + valor + " " + cmbVenta.getSelectedItem().toString() + " usted recibira " + conversion.VentaDolares(cmbVenta.getSelectedItem().toString(), valor) + " Dolares");
        
    }//GEN-LAST:event_btnVentaActionPerformed

