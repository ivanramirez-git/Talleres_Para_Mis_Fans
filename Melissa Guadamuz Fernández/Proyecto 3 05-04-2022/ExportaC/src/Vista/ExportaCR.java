    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //  informacion que se le solcitada al cliente:
        String nombre;
        String identificacion;
        int edad = -1;
        String direccion;
        boolean valido = true;
        
        try{
            identificacion = Id.getText();
            nombre = Nom.getText();
            edad = Integer.parseInt(Ed.getText());
            direccion = Dir.getText();
            if(!(identificacion.length()==10))
                valido = false;

            if(!(nombre.length()< 50))
                valido =false;

            if(!(edad >18 && edad < 100))
                valido = false;

            if (!(direccion.length()>= 20))
                valido = false;

            double kilogramos = Double.parseDouble(Kg2.getText());

            if(!(kilogramos >=1))
                valido = false;

            // Aqui se valida solamente un boton a seleccionar para la zona de exportancion
            Cotizacion c = usuario.getCotizacion();
            String direccion2 = En.getText();
            if (Ni.isSelected())
                c.setZona("Nueva Inglaterra");
            else if (Am.isSelected())
                c.setZona("Atlántico Medio");
            else if (As.isSelected())
                c.setZona("Atlántico Sur");
            else if (Cno.isSelected())
                c.setZona("Centro Norte Oriental");
            else if (!Cns.isSelected())
                c.setZona("Centro Sur Oriental");
            else if (!Cnoc.isSelected())
                c.setZona("Centro Norte Occidental");
            else if (!Cso.isSelected())
                c.setZona("Centro Sur Occidental");
            else if (!Mon.isSelected())
                c.setZona("Montaña");
            else if (!Pac.isSelected())
                c.setZona("Pacifico");
            else
                valido = false;

            if(!(direccion2.length()>= 20))
                valido = false;
            
            if(valido){
                // Cotizacion
                c.setDireccion(direccion2);
                c.setKg(kilogramos);
                // Usuario
                usuario.setNombre(nombre);
                usuario.setEdad(edad);
                usuario.setDireccion(direccion);
                usuario.setIdentificacion(identificacion);
                usuario.setCotizacion(c);
                // Mostrar Usuario
                Cliente.setText(usuario.datosCliente());
                CotizacionDatos.setText(usuario.getCotizacion().toString());
                Resultado.setText(usuario.getCotizacion().calcularExportacion());
            } else {
                JOptionPane.showMessageDialog(null, "Uno o mas valores ingresados no son validos");
            }
        } catch ( HeadlessException | NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Uno o mas valores ingresados no son validos");
        }   

    }//GEN-LAST:event_jButton1ActionPerformed

