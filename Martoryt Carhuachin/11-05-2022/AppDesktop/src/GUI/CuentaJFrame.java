    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try
        {
            String pelicula=txtPelicula.getText ();
            Double precio=Double.parseDouble(txtPrecio.getText());
            String plataforma=txtPlataforma.getText();
            
            SimpleDateFormat format=new SimpleDateFormat("yyyy-MM-dd");
            Date fvencimiento=format.parse(txtFvencimiento.getText());
            
            GregorianCalendar gc=new GregorianCalendar();
            gc.setTime(fvencimiento);
            
            XMLGregorianCalendar fvencimientoXML=DatatypeFactory.newInstance().newXMLGregorianCalendar(gc);
            
            Integer cuentasdisponibles=Integer.parseInt(txtCuentasdisponible.getText());
           
            CuentaVO cuenta=new  CuentaVO();
            
            cuenta.setPelicula(pelicula);
            cuenta.setPrecio(precio);
            cuenta.setPlataforma(plataforma);
            cuenta.setFvencimiento(fvencimientoXML);
            cuenta.setCuentasdisponibles(cuentasdisponibles);
            
            this.registrar(cuenta);
            
            JOptionPane.showMessageDialog(null,"¡El Nuevo Producto Fue Registrado Correctamente!");
            
            this.refrescarTable();
        }
        catch(Exception ex){ 
            ex.printStackTrace();
        }    
    }//GEN-LAST:event_btnGuardarActionPerformed

    public void refrescarTable()
    { 
        dtm.setRowCount(0);
        for(CuentaVO vo :listar())
        {
            dtm.addRow(new Object[]{
                vo.getCuentaId(),
                vo.getPelicula(),
                vo.getPrecio(),
                vo.getPlataforma(),
                vo.getFvencimiento(),
                vo.getCuentasdisponibles()
            });
        }
    }
    
