    private static java.util.List<JairWS.CuentaVO> listar() {
        JairWS.CuentaWS_Service service = new JairWS.CuentaWS_Service();
        JairWS.CuentaWS port = service.getCuentaWSPort();
        return port.listar();
    }

    private static String registrar(JairWS.CuentaVO arg0) {
        JairWS.CuentaWS_Service service = new JairWS.CuentaWS_Service();
        JairWS.CuentaWS port = service.getCuentaWSPort();
        return port.registrar(arg0);
    }
    
    
