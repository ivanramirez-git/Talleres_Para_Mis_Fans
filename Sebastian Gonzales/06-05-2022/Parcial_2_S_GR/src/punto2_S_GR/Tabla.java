    // Actualiza la tabla
    private void actualizarTabla() {
        // Encabezados
        String [] e = {"Nombre", "Ram", "Categoria"};
        // Datos
        Object [][] d = new Object[softwares.size()][3];
        // Recorremos la lista de softwares
        for (int i = 0; i < softwares.size(); i++) {
            // Obtenemos el softwares
            Software p = softwares.get(i);
            // Agregamos los datos
            d[i][0] = p.getNombre();
            d[i][1] = p.getRam();
            d[i][2] = p.getCategoria();
        }

        // Actualizamos la tabla
        tblPrincipal.setModel(new javax.swing.table.DefaultTableModel(
            d,
            e
        ));        
    }


    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        
        // Registramos a un software
        String nombre = JOptionPane.showInputDialog("Ingrese el nombre");
        // Validamos que el nombre no sea nulo
        if (nombre != null) {
            // Obtenemos la ram
            String altura = JOptionPane.showInputDialog("Ingrese la ram");
            // Validamos que la ram no sea nula
            if (altura != null) {
                // Validar que sea un número
                try {
                    // Convertimos la ram a double
                    double alturaD = Double.parseDouble(altura);
                    // Obtenemos el género
                    String genero = JOptionPane.showInputDialog("Ingrese la categoria L/E");
                    // Validamos que la categoria no sea nulo
                    if (genero != null) {
                        // Validamos que sea L o E
                        if (genero.equals("L") || genero.equals("E")) {
                            // Creamos el software
                            Software p = new Software(nombre, alturaD, genero);
                            // Agregamos el software a la lista
                            softwares.add(p);
                            // Actualizamos la tabla
                            actualizarTabla();
                        } else {
                            JOptionPane.showMessageDialog(this, "La categoria debe ser L o E");
                            return;
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "La categoria no puede ser nulo");
                        return;
                    }
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "La ram debe ser un número");
                    return;
                }
            } else {
                JOptionPane.showMessageDialog(this, "La ram no puede ser nula");
                return;
            }
        } else {
            JOptionPane.showMessageDialog(this, "El nombre no puede ser nulo");
            return;
        }

        // Actualizamos la tabla
        actualizarTabla();
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

